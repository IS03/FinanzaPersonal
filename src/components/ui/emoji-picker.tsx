interface EmojiPickerProps {
  onSelect: (emoji: string) => void
  trigger?: React.ReactNode
}

export function EmojiPicker({ onSelect, trigger }: EmojiPickerProps) {
  const [searchTerm, setSearchTerm] = useState("")
  const [isOpen, setIsOpen] = useState(false)

  const filteredEmojis = emojis.filter(emoji => 
    emoji.toLowerCase().includes(searchTerm.toLowerCase())
  )

  return (
    <Dialog open={isOpen} onOpenChange={setIsOpen}>
      <DialogTrigger asChild>
        {trigger || <Button variant="outline">Seleccionar Emoji</Button>}
      </DialogTrigger>
      <DialogContent className="sm:max-w-[425px]">
        <DialogHeader>
          <DialogTitle>Seleccionar Emoji</DialogTitle>
        </DialogHeader>
        <div className="py-4">
          <Input
            placeholder="Buscar emoji..."
            value={searchTerm}
            onChange={(e) => setSearchTerm(e.target.value)}
            className="mb-4"
          />
          <div className="grid grid-cols-8 gap-2 max-h-[300px] overflow-y-auto">
            {filteredEmojis.map((emoji, index) => (
              <Button
                key={index}
                variant="ghost"
                className="text-xl p-2 h-10 w-10"
                onClick={() => {
                  onSelect(emoji)
                  setIsOpen(false)
                }}
              >
                {emoji}
              </Button>
            ))}
          </div>
        </div>
      </DialogContent>
    </Dialog>
  )
} 